                    <h3>Perfil de egreso</h3>
                    <p>
                        Es un profesionista capaz de formular soluciones de diseño, desarrollo, mantenimiento e implantación de sistemas, productos y procesos mecatrónicos, mediante la integración sinérgica de elementos mecánicos, electrónicos y de control, que permitan la automatización de equipos y procesos, el control de sistemas flexibles de manufactura y el desarrollo de nuevos dispositivos con el fin de innovar, mejorar e impulsar tecnológicamente en su área laboral.
                    </p>

                    <h3>Las principales funciones que se desarrollan por un ingeniero mecatrónico son:</h3>
                    <ul>
                        <li>Diseñar sistemas, productos o procesos mecatrónicos, mediante la integración de tecnologías electrónicas, mecánicas y de control, para el desarrollo de los sectores social, público y privados.</li>

                        <li>Integrar elementos computacionales, electrónicos, mecánicos y de control, para mejorar el desempeño de sistemas o procesos mediante la automatización.</li>

                        <li>Administrar proyectos mecatrónicos, para la optimización de recursos o procesos mediante las metodologías y herramientas necesarias.</li>

                        <li>Proponer modelos o prototipos mecatrónicos, para innovar sistemas, mediante el desarrollo tecnológico e investigación aplicada.</li>

                        <li>Diseñar planes y programas de mantenimiento, para la prevención y corrección de fallas en sistemas mecatrónicos mediante las herramientas y normatividad aplicable.</li>

                        <li>Administrar el servicio de mantenimiento a sistemas mecatrónicos, para hacer eficientes los procesos de producción mediante la aplicación de los planes y programas de mantenimiento.</li>

                        <li>Construir elementos mecánicos y electrónicos, para su manufactura e integración en sistemas mecatrónicos, mediante herramientas computacionales y máquinas - herramientas.</li>

                        <li>Integrar robots, para su operación en sistemas de producción mediante su selección, instalación y programación.</li>
